int iic_device_write(u32 inst, u32 slave, u32 pll_src, u32 size, u32 data) {
  int XIicDevFile, ret = SUCCESS;
  char XIicDevFilename[20];
  unsigned char tx_array[4];

  sprintf(XIicDevFilename, "/dev/i2c-%d", XIIC_BUS);
  XIicDevFile = open(XIicDevFilename, O_RDWR);

  if (ioctl(XIicDevFile, I2C_SLAVE_FORCE, slave) < 0) {
    printf("Error: Could not set address \n");
    return FAIL;
  }
  if (size == 4) {
    tx_array[3] = (unsigned char)data & (0xFF);
    tx_array[2] = (unsigned char)(data >> 8) & (0xFF);
    tx_array[1] = (unsigned char)(data >> 16) & (0xFF);
    tx_array[0] = (unsigned char)(data >> 24) & (0xFF);
  } else if (size == 3) {
    tx_array[2] = (unsigned char)data & (0xFF);
    tx_array[1] = (unsigned char)(data >> 8) & (0xFF);
    tx_array[0] = (unsigned char)(data >> 16) & (0xFF);
  }

  IicWriteData(XIicDevFile, pll_src, size, tx_array);
  close(XIicDevFile);
  return ret;
}

char *StrRepl(char *str, char x, char y) {
  char *tmp = str;
  while (*tmp)
    if (*tmp == x)
      *tmp++ = y; /* assign first, then increment */
    else
      *tmp++;

  *tmp = '\0';
  return str;
}

int initRFclock(u32 board_id, u32 lmk_freq, u32 lmx0_freq, u32 lmx1_freq,
                u32 lmx2_freq) {
  unsigned int ret, i;

  /* set LMK clock */
  if ((lmk_freq >= LMK04208_MAX) || (board_id >= BOARD_ID_MAX))
    goto err;

  if ((lmx0_freq >= DAC_MAX) || (lmx1_freq >= DAC_MAX) ||
      (lmx2_freq >= DAC_MAX))
    goto err;

  /* configure LMK clock */
  ret = LMK04208ClockConfig(XIIC_BUS, &LMK04208_CKin[lmk_freq]);
  if (ret != SUCCESS) {
    printf("Unable to set LMK clock with %d offset", lmk_freq);
    goto err;
  }

  /* configure LMX 0 clock */
  ret = LMX2594ClockConfig(XIIC_BUS, PLL_A, &LMX2592_A[lmx0_freq]);
  if (ret != SUCCESS) {
    printf("Unable to set LMX 0 clock with %d offset", lmx0_freq);
    goto err;
  }

  /* configure LMX 1 clock */
  ret = LMX2594ClockConfig(XIIC_BUS, PLL_B, &LMX2592_A[lmx1_freq]);
  if (ret != SUCCESS) {
    printf("Unable to set LMX 1 clock with %d offset", lmx1_freq);
    goto err;
  }

  /* configure LMX 2 clock */
  ret = LMX2594ClockConfig(XIIC_BUS, PLL_C, &LMX2592_A[lmx2_freq]);
  if (ret != SUCCESS) {
    printf("Unable to set LMX 2 clock with %d offset", lmx2_freq);
    goto err;
  }

  DACCurrentFreq = lmx0_freq;
  ADC0CurrentFreq = lmx1_freq;
  ADC1CurrentFreq = lmx0_freq;


  /* Configure internal PLL */
  for(i= 0; i < DAC_MAX_TILE; i++) {
	  ret =  XRFdc_DynamicPLLConfig(&RFdcInst, 1, i, 1, 245.76, 3932.16);
  }



  return SUCCESS;

err:
  return FAIL;
}
